class User {
    constructor(props) {
        this.id = props.id;
        this.firstName = props.firstName;
        this.lastName = props.lastName;
        this.email = props.email;
        this.age = props.age;
        this.role = props.role;
        this.password = props.password;
        this.date = props.date;
    }
}

export default User;